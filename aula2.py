mensagem = 'eu adoro comida caseira'


print(mensagem.lower())#todas as letras do texto em minuscula

print(mensagem.upper())#todas as letras do texto em maiuscula

print(mensagem.capitalize())#Primeira letra da frase maiuscula

print(mensagem.find())#Sinaliza a posição da letra

print(mensagem.replace('a', 'e'))#Troca a letra(palavra) por outra 
 
print(mensagem.strip())#remove qualquer espaço que tenha antes do primeiro caractere
